#include <unistd.h>

void ft_is_negative(int n)
{
    char   neg;
    char   poz;

    neg = 'N';
    poz = 'P';
    
    if(n >= 0)
    {
        write(1, &poz, 1);
    }
    else 
    {
        write(1, &neg, 1);
    }
}



